package Section_03;

public class Ch18_Primitive_Types {

	public static void main(String[] args) {
		
		int min = Integer.MIN_VALUE;
		int max = Integer.MAX_VALUE;
						
		System.out.println("Val minima pt Integer este: " + min);
		System.out.println("Val maxima pt Integer este: " + max);
		
		System.out.println("Intervalul pentru Integer este: " + " < " + min + " , " + max + " > ");
		
		int wow = max+11;
		
		System.out.println("Valoarea lui wow este: " + wow);
		
		int q1=1_234; int q2=22_333;
		
		System.out.println("Valoarea lui q1 este: " + q1);
		System.out.println("Valoarea lui q2 este: " + q2);
	}
}
